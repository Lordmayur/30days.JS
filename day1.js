//creating variables of different data types and log each variable using typeof operator.
 
//string
let mystring="jack"
console.log(typeof mystring);

//number
let mynum=7;
console.log(typeof mynum);

//boolean
let mybool=true;
console.log(typeof mybool);

// Object
const myObject = {
    name: "John",
    age: 30,
    isStudent: true
  };
  console.log(typeof myobject); 
  
// Array
const myArray = [1, 2, 3, 4, 5];
